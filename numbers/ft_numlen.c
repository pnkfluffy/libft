#include "libft.h"

/*
**	Returns the number of digits in an int
*/

int	ft_numlen(int n)
{
	int i;

	i = 1;
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	return (i);
}
